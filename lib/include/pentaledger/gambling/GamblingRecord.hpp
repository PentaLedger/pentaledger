/** \defgroup gambling Gambling
 * \brief Gambling Wins and Losses
 *
 * Taxpayers must report the full amount of
 * gambling winnings for the year on Form
 * 1040, U.S. Individual Income Tax Return, line 21.
 *
 * A deduction for gambling losses for the year
 * is allowed on Schedule A (Form 1040), Itemized Deductions,
 * line 28, up to the amount of winnings.
 *
 * Gambling winnings cannot be reduced by
 * gambling losses and reported as the difference. Taxpayers
 * must report the full amount of winnings as income and claim losses (up
 * to the amount of winnings) as an itemized
 * deduction. Therefore, records should show
 * the winnings separately from the losses.
 * Diary of winnings and losses. Taxpayers
 * must keep an accurate diary or similar record of losses and winnings. The diary should
 * contain at least the following information.
 *
 * - The date and type of specific wager or wagering activity.
 * - The name and address or location of the gambling establishment.
 * - The names of other persons present with the taxpayer at the gambling establishment.
 * - The amount(s) won or lost.
 *
 * Proof of winnings and losses. In addition
 * to the diary, taxpayers should also have other documentation to establish proof of
 * winnings and losses.
 *
 * - Form W-2G, Certain Gambling Winnings.
 * - Form 5754, Statement by Person(s) Receiving Gambling Winnings.
 * - Wagering tickets, cancelled checks, substitute checks, credit records, bank
 *   withdrawals, and statements of actual winnings or
 *   payment slips provided by the gambling establishment.
 *
 **/

